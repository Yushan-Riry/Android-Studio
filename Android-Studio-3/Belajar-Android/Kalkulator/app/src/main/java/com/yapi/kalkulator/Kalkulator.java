package com.yapi.kalkulator;

public class Kalkulator {

    public double penjumlahan (double bil_1, double bil_2) { return bil_1 + bil_2; }

    public double pengurangan (double bil_1, double bil_2) { return bil_1 - bil_2; }

    public double perkalian (double bil_1, double bil_2) { return bil_1 * bil_2; }

    public double pembagian (double bil_1, double bil_2) { return bil_1 / bil_2; }

}
